export function computeStats(orders) {
  const totalRevenue = orders.reduce((s, o) => s + (o.total_price || 0), 0);
  const totalPaid    = orders.reduce((s, o) => s + (o.paid_amount  || 0), 0);
  const statusCounts = orders.reduce((acc, o) => {
    acc[o.status] = (acc[o.status] || 0) + 1;
    return acc;
  }, {});
  return {
    total_orders: orders.length,
    total_revenue: totalRevenue,
    total_paid: totalPaid,
    by_status: Object.entries(statusCounts).map(([status, count]) => ({ status, count })),
    by_stage: [],
  };
}
